#include "select.h"

char	*radical(char *av, int e, int *l)
{
	*l = 0;
	while (&av[e - *l] >= av && av[e - *l] != '/')
		*l += 1;
	if (*l && av[e - *l] == '/')
		*l += 1;
	else if (*l == 0)
		return (av);
	return (&av[e - *l]);
}
